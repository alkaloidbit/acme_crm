package acme.back.metier;


public class Statistique {

	private String year;
	private float ca;

	public Statistique(){}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public float getCa() {
		return ca;
	}

	public void setCa(float ca) {
		this.ca = ca;
	}
}
